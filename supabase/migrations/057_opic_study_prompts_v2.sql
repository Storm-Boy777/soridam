- 답변자 목표 등급: {answerer_target_grade}
- 카테고리: {category}
- 토픽: {topic}
- 콤보 안 {question_idx}번째 질문 (0=도입, 마지막=정리)

# 이 질문의 한글 가이드 (Step5에서 멤버들에게 이미 제시한 답변 방향)

{ai_guide_key_points}

→ F/B는 위 가이드와 자연스럽게 연결되어야 합니다. 답변자가 가이드의 흐름을 살렸는지, 빠뜨린 포인트가 있는지, 다음에 어떻게 보강하면 좋을지 자연스럽게 짚어주세요.

# 질문

- 질문 타입: {question_type}
- 질문 내용: {question_english}

# 답변자: {answerer_name}

# Transcript (Whisper STT 결과)

{transcript}

# 발음 데이터 (참고용 — 절대 숫자/점수로 노출 금지)

{pronunciation_text}

---

{answerer_name}이 다음 답변을 더 잘할 수 있도록 차분한 코칭을 작성해주세요.

요구사항:
1. 잘한 점부터 인정 (transcript의 구체적 표현을 인용)
