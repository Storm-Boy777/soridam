3. 다음 답변에 즉시 적용할 팁 1~2개
4. 발음 데이터 활용: 약점 단어 있으면 "OO 한 번 더 또박" 식으로 자연어 변환
5. 차분한 톤 유지 (과한 감탄사 X, 절제된 격려)

JSON 형식으로만 응답.$$,
    updated_at = now()
WHERE key = 'opic_study_feedback_user';
