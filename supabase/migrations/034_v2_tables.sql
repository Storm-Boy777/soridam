CREATE OR REPLACE FUNCTION public.v2_polar_deduct_balance(p_user_id uuid, p_cost_krw integer, p_description text DEFAULT '크레딧 사용', p_ref_id text DEFAULT NULL)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
