-- v2_process_payment
CREATE OR REPLACE FUNCTION public.v2_process_payment(
  p_user_id uuid, p_payment_id text, p_product_id text, p_order_name text, p_amount integer,
  p_pg_provider text DEFAULT NULL, p_pg_tx_id text DEFAULT NULL, p_pay_method text DEFAULT NULL,
  p_paid_at timestamptz DEFAULT NULL, p_receipt_url text DEFAULT NULL,
  p_is_plan boolean DEFAULT false, p_plan text DEFAULT 'free', p_plan_months integer DEFAULT 0,
  p_mock_exam_credits integer DEFAULT 0, p_script_credits integer DEFAULT 0, p_tutoring_credits integer DEFAULT 0
)
RETURNS jsonb
LANGUAGE plpgsql
SECURITY DEFINER
AS $function$
DECLARE
  v_order_id UUID;
  v_expires_at TIMESTAMPTZ;
BEGIN
  INSERT INTO v2_orders (
    user_id, product_id, order_name, amount, status,
    payment_id, pg_provider, pg_tx_id, pay_method, paid_at, receipt_url
  ) VALUES (
    p_user_id, p_product_id, p_order_name, p_amount, 'paid',
    p_payment_id, p_pg_provider, p_pg_tx_id, p_pay_method,
    COALESCE(p_paid_at, NOW()), p_receipt_url
  )
  ON CONFLICT (payment_id) DO NOTHING
  RETURNING id INTO v_order_id;

  IF v_order_id IS NULL THEN
    RETURN jsonb_build_object('success', true, 'duplicate', true);
  END IF;

  IF p_is_plan THEN
    v_expires_at := NOW() + (p_plan_months || ' months')::INTERVAL;
    UPDATE v2_user_credits SET
      current_plan = p_plan,
      plan_mock_exam_credits = p_mock_exam_credits,
      plan_script_credits = p_script_credits,
      plan_tutoring_credits = p_tutoring_credits,
      plan_expires_at = v_expires_at,
      updated_at = NOW()
    WHERE user_id = p_user_id;
  ELSE
    UPDATE v2_user_credits SET
      mock_exam_credits = mock_exam_credits + p_mock_exam_credits,
      script_credits = script_credits + p_script_credits,
      tutoring_credits = tutoring_credits + p_tutoring_credits,
      updated_at = NOW()
    WHERE user_id = p_user_id;
  END IF;

  RETURN jsonb_build_object('success', true, 'order_id', v_order_id::text);
END;
$function$;

