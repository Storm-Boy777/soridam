-- v2_deduct_usage_cost
CREATE OR REPLACE FUNCTION public.v2_deduct_usage_cost(p_user_id uuid, p_cost_krw integer)
RETURNS TABLE(success boolean, remaining_balance integer)
LANGUAGE plpgsql
SECURITY DEFINER
AS $function$
DECLARE
  v_current_balance INTEGER;
BEGIN
  SELECT balance_krw INTO v_current_balance
  FROM v2_user_credits
  WHERE user_id = p_user_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RETURN QUERY SELECT false, 0;
    RETURN;
  END IF;

  IF v_current_balance < p_cost_krw THEN
    RETURN QUERY SELECT false, v_current_balance;
