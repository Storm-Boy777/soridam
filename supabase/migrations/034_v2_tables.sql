-- v2_get_user_cost_summary
CREATE OR REPLACE FUNCTION public.v2_get_user_cost_summary(p_user_id uuid, p_from timestamptz DEFAULT (now() - '30 days'::interval), p_to timestamptz DEFAULT now())
RETURNS TABLE(session_type text, total_calls bigint, total_cost_usd numeric, total_cost_krw numeric)
LANGUAGE sql
STABLE
AS $function$
  SELECT
    session_type,
    COUNT(*) AS total_calls,
    SUM(cost_usd) AS total_cost_usd,
    ROUND(SUM(cost_usd) * 1400, 0) AS total_cost_krw
  FROM v2_api_usage_logs
  WHERE user_id = p_user_id
    AND created_at >= p_from
    AND created_at < p_to
  GROUP BY session_type
  ORDER BY total_cost_krw DESC;
$function$;

-- v2_get_user_usage_summary
CREATE OR REPLACE FUNCTION public.v2_get_user_usage_summary(p_user_id uuid, p_from timestamptz DEFAULT (now() - '30 days'::interval), p_to timestamptz DEFAULT now())
RETURNS TABLE(session_type text, feature text, call_count bigint, total_cost_usd numeric, total_tokens_in bigint, total_tokens_out bigint, total_audio_sec numeric)
LANGUAGE sql
STABLE
AS $function$
  SELECT
    session_type,
    feature,
    COUNT(*) AS call_count,
    SUM(cost_usd) AS total_cost_usd,
    SUM(tokens_in) AS total_tokens_in,
    SUM(tokens_out) AS total_tokens_out,
    SUM(audio_duration_sec) AS total_audio_sec
  FROM v2_api_usage_logs
  WHERE user_id = p_user_id
    AND created_at >= p_from
    AND created_at < p_to
  GROUP BY session_type, feature
  ORDER BY total_cost_usd DESC;
$function$;

