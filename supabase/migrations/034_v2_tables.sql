-- v2_charge_balance
CREATE OR REPLACE FUNCTION public.v2_charge_balance(p_user_id uuid, p_amount_krw integer)
RETURNS integer
LANGUAGE plpgsql
SECURITY DEFINER
AS $function$
DECLARE
  v_new_balance INTEGER;
BEGIN
  UPDATE v2_user_credits
  SET balance_krw = balance_krw + p_amount_krw,
      updated_at = NOW()
  WHERE user_id = p_user_id
  RETURNING balance_krw INTO v_new_balance;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'v2_user_credits not found for user %', p_user_id;
  END IF;

  RETURN v_new_balance;
END;
$function$;

-- v2_check_balance
CREATE OR REPLACE FUNCTION public.v2_check_balance(p_user_id uuid)
RETURNS integer
LANGUAGE sql
