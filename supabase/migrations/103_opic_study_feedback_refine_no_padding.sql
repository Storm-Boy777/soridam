-- ============================================================
-- 오픽 스터디 피드백 v5.1 — refine_expressions 억지 채우기 금지
-- ============================================================
--
-- 배경: 5건 답변 대비 코치 노트 검증 (docs/오픽스터디_코칭노트_개선안.md §부록)
--   refine 품질이 답변 수준과 반비례 — 오류 많은 답변엔 정확, 이미 잘한
--   답변엔 1~3개 쿼터를 채우려 가짜 오류를 생성.
--   실측 예: "had overheated"→"got too hot"(좋은 어휘 다운그레이드),
--            "three-star Michelin recipes"(의도된 유머를 '교정'),
--            "I was pretty annoyed"→감정 더 풍부히(콘텐츠 제안을 문법오류로 포장).
--
-- 변경: refine_expressions 섹션에
--   1) 헤더 개수 가이드 1~3개 → 0~3개
--   2) "억지로 채우지 마라 / 없으면 빈 배열" + 스타일·유머·콘텐츠 제안 금지 지시 추가
--
-- 전체 재작성 대신 v5(마이그 102) 텍스트의 해당 부분만 REPLACE.
-- EF 재배포 불필요 (런타임에 row 로드).
-- ============================================================

UPDATE evaluation_prompts
SET prompt_text = replace(
  replace(
    prompt_text,
    '## 4. refine_expressions (함께 다듬어볼 표현 — 문법/어휘) 1~3개',
    '## 4. refine_expressions (함께 다듬어볼 표현 — 문법/어휘) 0~3개'
  ),
  '**우선순위 정렬 (OPIc 등급 영향이 큰 순으로 — 첫 번째에 가장 임팩트 큰 것)**:',
  '**억지로 채우지 마세요.** 진짜 문법/어휘 오류만 담으세요. 오류가 1개뿐이면 1개만, 정말 없으면 빈 배열 []로 두세요. 다음은 refine에 넣지 마세요 (넣으려면 discussion_hooks나 next_speaker_tip.enhance로):
- 이미 자연스러운 표현을 스타일·취향 문제로 트집 잡기 (특히 좋은 어휘를 더 쉬운 말로 다운그레이드 — 답변자 목표는 AL이라 어휘 격하는 역효과)
- 의도된 유머·과장을 "교정"하기
- "감정을 더 풍부히" 같은 콘텐츠·내용 보강 제안을 문법 오류처럼 포장하기

**우선순위 정렬 (OPIc 등급 영향이 큰 순으로 — 첫 번째에 가장 임팩트 큰 것)**:'
)
WHERE key = 'opic_study_feedback';
